DECLARE
    v_cnt NUMBER;
BEGIN
    SELECT COUNT(*)
    INTO v_cnt
    FROM DBA_USERS
    WHERE USERNAME = 'C##TMPUSER';

    IF v_cnt > 0 THEN
        EXECUTE IMMEDIATE 'DROP USER C##TMPUSER CASCADE';
    END IF;
END;
/
CREATE USER C##TMPUSER IDENTIFIED BY CorrectHorseBatteryStaple QUOTA UNLIMITED ON SYSTEM
/
GRANT CREATE SESSION TO C##TMPUSER
/
GRANT CREATE TABLE TO C##TMPUSER
/
GRANT CREATE VIEW TO C##TMPUSER
/
GRANT CREATE ANY TRIGGER TO C##TMPUSER
/
GRANT CREATE ANY PROCEDURE TO C##TMPUSER
/
GRANT CREATE SEQUENCE TO C##TMPUSER
/
GRANT CREATE SYNONYM TO C##TMPUSER
/
GRANT UNLIMITED TABLESPACE TO C##TMPUSER
